from collections import deque
import sys
input = sys.stdin.readline

n,m = map(int,input().split())
graph = [[] for _ in range(n+1)]
for _ in range(m):
    a,b = map(int,input().split())
    graph[a].append(b)
    graph[b].append(a)
    
visited = [False]*(n+1)
cnt = 0
def bfs(n):
    q = deque([n])
    visited[1] = True
    while q:
        v = q.popleft()
        for nv in graph[v]:          
            if not visited[nv]:
                visited[nv] = True
                q.append(nv)
                
for i in range(1,n+1):
    if not visited[i]:
        bfs(i)
        cnt +=1
print(cnt)
    
            
    
    